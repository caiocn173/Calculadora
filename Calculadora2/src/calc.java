    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        if(op.equals("soma")) {
         float calc = varSoma +  Float.valueOf(jTextField1.getText());
         jTextField1.setText("" + calc);
         op = "";
        } else if(op.equals("sub")) {
         float calc = varSub - Float.valueOf(jTextField1.getText());
         jTextField1.setText("" + calc); 
         op = "";
        } else if(op.equals("mult")){
         float calc = varMult * Float.valueOf(jTextField1.getText());
         jTextField1.setText("" + calc); 
         op = "";   
        } else if(op.equals("div")) {
         float calc = varDiv / Float.valueOf(jTextField1.getText());
         jTextField1.setText("" + calc); 
         op = "";
        }
    }//GEN-LAST:event_jButton16ActionPerformed

